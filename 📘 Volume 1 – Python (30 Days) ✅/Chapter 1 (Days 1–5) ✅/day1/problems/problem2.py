# What is REPL?

# REPL stands for:

# R → Read
# E → Evaluate
# L → Loop

# Let's understand each word.

# 1. Read

# Python reads whatever you type.

# For example, you type:

# 5 + 10

# Python reads:

# "Okay, the user typed 5 + 10."

# 2. Evaluate

# Python calculates or executes what you typed.

# It evaluates:

# 5 + 10

# Result:

# 15
# 3. Print

# Python shows the result.

# 15
# 4. Loop

# Python doesn't stop.

# It waits for the next command.

# >>>

# You type something again.

# Python repeats the process.

# This cycle continues until you exit.

